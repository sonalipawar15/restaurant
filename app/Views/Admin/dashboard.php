<script>
  $(function(){
  $(".count").hide(0).fadeIn(3000)
    $('.count').each(function() {
      var $this = $(this),
          countTo = $this.attr('data-count');
      $({ countNum: $this.text()}).animate({
        countNum: countTo
      },
      {
        duration: 3000,
        easing:'linear',
        step: function() {
          $this.text(Math.floor(this.countNum));
        },
        complete: function() {
          $this.text(this.countNum.toString().replace(/\B(?=(\d{3})+(?!\d))/g, ","));
        }
      });
    });
});
</script>
